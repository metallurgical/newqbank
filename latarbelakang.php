        <p><strong>1 LATAR BELAKANG SEKOLAH</strong></p>
	<p>&nbsp;</p>
	<p><strong>1.1	 SEJARAH SEKOLAH</strong></p>
	<p> Sekolah Menengah Sains Dungun terletak di kawasan seluas 47.048 ekar di Mukim Sura Tengah, Dungun, Terengganu. Kerja-kerja pembinaan sekolah ini telah bermula pada bulan Januari 1980 dan akhirnya siap sepenuhnya pada bulan Jun 1982. Persediaan untuk memulakan persekolahan bermula pada 1 Mei 1982 dimana beberapa orang guru dan kakitangan sokongan telah ditempatkan di sekolah ini. Akhirnya, pada 15 Ogos 1982, Sekolah Menengah Sains Dungun dengan rasminya memulakan sesi pengajiannya.</p>
	<p>&nbsp;</p>
	<p> Kumpulan pertama pelajar yang mendaftar diri pada 12 Ogos 1982 ialah seramai 450 orang yang terdiri daripada pelajar-pelajar tingkatan satu, empat dan enam rendah. Sejak dari tahun 1982 hingga sekarang sekolah ini telah mengalami pertumbuhan dan perkembangan yang pesat dari segi enrolmen pelajar, bilangan kelas, guru dan kakitangan sekolah.</p>
	<p> Pada 15 Jun 2005, sekolah ini sekali lagi mencatat sejarah apabila Persatuan Bekas Pelajar telah didaftarkan secara rasmi dengan pihak Pendaftar Pertubuhan.	</p>
    
	<p><strong>1.2 FALSAFAH SEKOLAH MENENGAH SAINS DUNGUN</strong></p>
	<p>Berusaha mewujudkan suasana persekolahan yang terancang dan sempurna supaya  pendidikan dapat memperkembangkan potensi pelajar-pelajar secara menyeluruh dan bersepadu untuk melahirkan insan yang sempurna.</p>
	<p> </p>
    
	<p><strong>1.3 WAWASAN SEKOLAH MENENGAH SAINS DUNGUN</strong></p>
	<p>Wawasan Sekolah Menengah Sains Dungun ialah untuk menuju ke arah untuk menjadikan sekolah ini sebagai :	</p>
	<ul>
	  <li> Pusat pembangunan manusia cemerlang</li>
	  <li> Sumber penyuburan nilai-nilai murni bangsa</li>
	  <li> Arena penyuburan watak manusia penyayang</li>
	  <li> Merangka penghayatan semangat cinta negarI	</li>
	</ul>
    
	<p><strong>1.4 MISI SEKOLAH MENENGAH SAINS DUNGUN</strong></p>
	<p>Bahawa sesungguhnya misi Sekolah Menengah Sains Dungun ialah untuk meningkatkan kecekapan dan keberkesanan dalam melaksanakan serta mengendali program pendidikan.Ini adalah bertujuan untuk melahirkan insan yang cemerlang, mulia dan berkualiti tinggi dalam ilmu pengetahuan agar mereka menjadi insan yang kreatif, inivatif dan proaktif dalam menangani cabara-cabaran Wawasan 2020.	</p>
    
	<p><strong>2.3 OBJEKTIF</strong></p>
	<p>i] Membentuk sifat berdikari dan bertanggungjawab dalam pengurusan Rumah sebagai sebuah organisasi kecil dengan mengamalkan bentuk pentadbiran yang teratur dan tersusun</p>
    
	<p>ii] Mewujudkan semangat kekitaan dan sentiasa berubah mencari kecemerlangan dalam hidup</p>
	<p>iii] Melahirkan pelajar yang seimbang, bersepadu dari segi rohani dan jasmani</p>
    
	<p>iv] Meningkatkan &ldquo;self-esteem&rdquo; pelajar bagi melahirkan pelajar yang berkeyakinan untuk menjadi individu dan pemimpin yang cemerlang</p>
	<p>v] Melahirkan perwatakan sekolah yang unik dan istimewa</p>
	<p>vi] Mengukuhkan interaksi dan integrasi di kalangan pelajar</p>
	<p>vii] Mewujudkan suasana atau iklim sekolah yang lebih kondusif untuk aktiviti pembelajaran dan pengajaran</p>
    
	<p>viii] Mewujudkan budaya sekolah yang berteraskan keunggulan dan kecemerlangan	</p>
    
	<p><strong>3.1 AKADEMIK</strong></p>
	<p>Setiap pencapaian pelajar dalam peperiksaan atau ujian yang dikendalikan pihak sekolah akan diberikan mata ganjaran. Pencapaian yang baik akan memberikan ganjaran mata yang tinggi dan ini menyebabkan pelajar-pelajar akan berusaha untuk mencapai keputusan akademik yang baik kerana ia akan menyumbang ke arah kecemerlangan Rumah mereka.	</p>
    
	<p><strong>3.2 KOKURIKULUM</strong></p>
	<p>Aktiviti kokurikulum dan koakademik menjadi salah satu teras ke arah mewujudkan budaya sekolah yang sihat dan melahirkan pelajar yang seimbang. Setiap penglibatan pelajar dalam aktiviti kokurikulum akan memberikan mata ganjaran kepada Rumah-Rumah mereka.  Aktiviti-aktiviti kokurikulum yang disertai pelajar membabitkan aktiviti-aktiviti yang dianjurkan Kementerian Pelajaran [ dibawah MSSM, MSST &amp; MSSD], kerajaan negeri, pihak swasta, badan-badan berkanun, pertubuhan-pertubuhan bukan kerajaan dan juga aktiviti-aktiviti anjuran kelab, persatuan serta unit uniform sekolah. Antara aktiviti-aktiviti pertandingan kokurikulum antara Rumah yang dijalankan oleh pihak sekolah sendiri ialah :	</p>
    
	<p>Penyertaan pelajar-pelajar dalam setiap pertandingan mewujudkan persaingan yang sihat di antara mereka. Kebolehan dan kemahiran pelajar dalam kokurikulum juga dapat dicungkil melalui penyertaan mereka dan ini memudahkan pengurusan sekolah menyediakan pasukan-pasukan sekolah yang mantap.	</p>
    
	<p><strong>3.4 SAHSIAH &amp; KEPIMPINAN</strong></p>
	<p>Salah satu objektif Pelaksanaan Sistem Rumah ini ialah untuk meningkatkan semangat kekitaan [ sense of belonging] di kalangan pelajar dengan mewujudkan perasaan bertanggungjawab dalam diri mereka. Pelaksanaan sistem ini memberi peluang kepada pelajar untuk membuktikan kepemimpinan dan perasaan tanggungjawab mereka dengan wujudnya jawatan-jawatan seperti AJK Blok, ketua dorm, ketua aras dan sebagainya.  Disamping jawatan-jawatan tradisi seperti Pengawas, Pustakawan dan AJKM, semua jawatan-jawatan ini akan menyumbang ke markah keseluruhan Rumah. Pelajar-pelajar yang menyandang jawatan-jawatan khas dalam pasukan-pasukan sukan, kelab, persatuan serta unit beruniform turut akan menyumbang markah ke Rumah masing-masing.</p>
	<p><strong>5 PENGIKTIRAFAN</strong></p>
    
	<p>Kerjasama, komitmen dan penglibatan pelajar adalah amat penting</p>
	<p>untuk menjayakan Pelaksanaan Sistem Rumah ini. Untuk mengharagai komitmen pelajar, pihak sekolah membuat beberapa pengiktirafan terhadap usaha mereka. Hadiah dan anugerah diberikan kepada pelajar-pelajar yang berjaya menunujukkan kecemerlangan dalam aktiviti-aktiviti yang dilaksanakan di bawah sistem ini. Diantara pengiktirafan yang diberikan ialah ;	</p>
	<p><strong>8 RUMUSAN</strong></p>
    
	<p>Pelaksanaan Sistem Rumah di Sekolah Menengah Sains Dungun dibuat</p>
	<p>Sebagai menyahut arahan pihak Sektor Sekolah Berasrama Penuh yang mahukan sistem ini dilaksanakan di setiap SBP.  Kejayaan pelaksanaan sistem ini banyak bergantung kepada komitmen semua pihak di sekolah ini. Kesungguhan yang ditunjukkan oleh pihak pengurusan sekolah, guru-guru dan kakitangan sokongan akan memberi impak yang positif kepada para pelajar.</p>
	<p> Pelaksanaan Sistem Rumah merupakan satu idea bijak yang berupaya meningkatkan pembangunan darjah emosi pelajar bagi menghasilkan pelajar-pelajar yang memilikki jati diri yang kukuh, berketrampilan, berhemah mulia, bertimbang rasa dalam kehidupan bermasyarakat dan mempunyai daya kepimpinan yang tinggi, sebagai sumber tenaga cemerlang dan bijaksana kepada pembangunan negara di masa hadapan.</p>
